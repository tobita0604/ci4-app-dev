  <h3>＜概要＞</h3>
  <p>セブ島はビサヤ諸島の中心部に位置する島で、マクタン島やオランゴ環礁など167の島々とあわせてセブ州に属しています。ビサヤ地方の政治経済、文化の中心地となっています。リゾート地としても有名で、セブ島と橋で結ばれたマクタン島の東海岸にはリゾートホテルが多く並んでいます。セブ島は南北に長く、225キロメートルにわたります。<br>
セブ島の中心となる都市はセブ市（セブシティー）です。セブ市の人口は約87万人でフィリピンで5番目に多く、セブ市周辺のマンダウエ市、ラプ＝ラプ市、タリサイ市などの市とあわせて「メトロ・セブ」と呼ばれ、マニラに次ぐ、フィリピン第2の都市を形成しています。リゾート地としての「セブ」はマクタン島東海岸のリゾート地のことを指します。リゾート地から、セブ島のセブシティーまでは車で30分ほど。ショッピングモールで、ショッピングも楽しめます。 </p>
