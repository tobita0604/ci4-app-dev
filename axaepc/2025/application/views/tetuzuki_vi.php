	  <h3>③受領</h3>
	  <p>申請後、受領までに、通常1週間程度かかります。(土・日・休日を除く)　 <br>
	    必ずご本人が発給手数料(10年旅券16,000円/５年旅券11,000円/12歳未満5年旅券6,000円) を <br>
