  <h2 class="h2adjust">■パスポートの申請手続きについて</h2>
<span style ="font-weight:bold;font-size:16px;color:#FF0000;text-decoration: underline;text-decoration-style: wavy;">今回のご旅行には、2018年8月9日まで残存のあるパスポートが必要です。</span></br>
また、未使用査証欄が見開き2ページ以上が必要です。<br>
申請から受領までに、通常１週間程度（土・日・休日を除く）かかります。遅くても、12月19日(火)までには申請をお済ませの上、12月27日(水)までに、ツアーサイトのマイページからアップロードしてください。<br>
有効期限が不足している方は、申請手続きをお願いします。
  <p>下記の書類をご持参の上、住民票登録の都道府県パスポート担当窓口にてご申請ください。 なお、一部の都道府県でパスポート業務(申請受付・交付)が都道府県のパスポート担当窓口から市区町村へ移譲されておりますので、ご注意ください。 </p>
